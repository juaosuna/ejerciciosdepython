valor1 = int(input('Digite primer numero: '))
valor2 = int(input('Digite segundo numero: '))

resultado = valor1 + valor2
print(valor1, valor2, sep='+', end='=')
print(resultado)

resultado = valor1 - valor2
print(valor1, valor2, sep='-', end='=')
print(resultado)

resultado = valor1 * valor2
print(valor1, valor2, sep='*', end='=')
print(resultado)

resultado = valor1 / valor2
print(valor1, valor2, sep='/', end='=')
print(resultado)
